package com.spd.grid.model;

public class Hail {
		private double lon;//经度
		private double lat;//纬度
		private double smallProbability;//小冰雹概率
		private double bigProbability;//大冰雹概率
		private double level;//级别
		public Hail(double lon, double lat, double smallProbability,
				double bigProbability, double level) {
			super();
			this.lon = lon;
			this.lat = lat;
			this.smallProbability = smallProbability;
			this.bigProbability = bigProbability;
			this.level = level;
		}
		public double getLon() {
			return lon;
		}
		public void setLon(double lon) {
			this.lon = lon;
		}
		public double getLat() {
			return lat;
		}
		public void setLat(double lat) {
			this.lat = lat;
		}
		public double getSmallProbability() {
			return smallProbability;
		}
		public void setSmallProbability(double smallProbability) {
			this.smallProbability = smallProbability;
		}
		public double getBigProbability() {
			return bigProbability;
		}
		public void setBigProbability(double bigProbability) {
			this.bigProbability = bigProbability;
		}
		public double getLevel() {
			return level;
		}
		public void setLevel(double level) {
			this.level = level;
		}
}
