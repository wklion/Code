package com.spd.grid.domain;

public class Legend {
	private double v;
	private int r;
	private int g;
	private int b;
	public double getV() {
		return v;
	}
	public void setV(double v) {
		this.v = v;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
	public int getG() {
		return g;
	}
	public void setG(int g) {
		this.g = g;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
}
